    private void viewData(String email){
        if (!(customerEmail.equals(""))) {

            boolean result = Searchcustomer(customerEmail);
            if (result) {
                customerDetails(customerEmail);
                this.viewHistory.requestFocusInWindow();
            } else {
                JOptionPane.showMessageDialog(this, "Invalid customer search");
                this.email.setText("");
                this.email.requestFocusInWindow();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill the field");
        }
    }
    
