    private boolean Searchcustomer(String email) {

        boolean flag = false;
        try {
            Connection con = Database.getConnected();
            String query = "SELECT email FROM customer_login WHERE "
                    + "email='" + email + "'";
            ResultSet rs = Database.executeQuery(con, query);
            if (!rs.next()) {
                flag = false;
            } else {
                flag = true;
            }
        } catch (SQLException | ClassNotFoundException ex) {
        }
        return flag;
    }

    private void customerDetails(String email) {
        try {
            Connection con;
            con = Database.getConnected();
            ResultSet rs = Database.executeQuery(con, "SELECT * FROM `customer_login` WHERE `email`='" + email + "'");
            rs.next();

            this.emailAd1.setVisible(true);
            this.name1.setVisible(true);
            this.nic1.setVisible(true);
            this.mobileNo1.setVisible(true);
            this.viewHistory.setVisible(true);
            name.setText(rs.getString(1));
            emailAd.setText(rs.getString(2));
            nic.setText(rs.getString(4));
            mobileNo.setText(rs.getString(3));

        } catch (SQLException | ClassNotFoundException ex) {
        }
    }

    private void setResultTable() {

        String[] header = {"Full Name", "Email", "Mobile No", "NIC Number"};
        DefaultTableModel dtm = new DefaultTableModel() {
            public boolean isCelEditable(int row, int coll) {
                return coll != 1;
            }
        };
        dtm.setColumnIdentifiers(header);
        customerData.setModel(dtm);

        try {
            String[][] results = viewtable();

            for (String[] result : results) {
                dtm.addRow(result);
                customerData.setModel(dtm);
            }
            Dimension dim = customerData.getSize();
            int height = (customerData.getRowCount() * 16) + 24;
            if (height > 300) {
                height = 300;
            }
            jScrollPane1.setSize(dim.width, height);

        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            ex.printStackTrace();
        }
    }

    private static String[][] viewtable()
            throws SQLException, ClassNotFoundException {
        Connection con;
        ResultSet rs;
        String query;
        String[][] results = null;

        con = Database.getConnected();
        query = "SELECT * FROM customer_login";
        rs = Database.executeQuery(con, query);
        if (rs != null) {
            int count;
            for (count = 0; rs.next(); count++) {
            }
            rs = Database.executeQuery(con, query);
            results = new String[count][4];
            for (int i = 0; rs.next(); i++) {
                results[i][0] = rs.getString(1);
                results[i][1] = rs.getString(2);
                results[i][2] = rs.getString(3);
                results[i][3] = rs.getString(4);

            }
        }
        Database.disconnect(con);
        return results;
    }

