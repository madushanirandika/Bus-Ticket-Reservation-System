    private boolean customerForgotPassword(String password1) {
        boolean flag = false;
        Connection con;
        int rs;
        String passWord1 = SHA.getSHAPassword(password1);
        try {
            con = Database.getConnected();
            String query = "UPDATE customer_login SET password='" + passWord1 + "' "
                    + "WHERE email='" + customerEmail + "'";
            rs = Database.updateQuery(con, query);
            if (rs == 1) {
                flag = true;
            }
        } catch (SQLException | ClassNotFoundException ex) {
        }
        return flag;
    }

