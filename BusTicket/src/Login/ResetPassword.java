    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        if (!(password.getText().equals("")
                || verifyPassword.getText().equals(""))) {

            String password1 = String.valueOf(password.getPassword());
            String password2 = String.valueOf(verifyPassword.getPassword());

            if (password1.equals(password2)) {
                boolean flag = customerForgotPassword(password1);

                if (!flag) {
                    JOptionPane.showMessageDialog(this, "Error! Try again with "
                            + "correct data");
                } else {
                    JOptionPane.showMessageDialog(this, "New password "
                            + "successfully!");
                    StartBooking x = new StartBooking(customerEmail);
                    x.setVisible(true);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please enter correct "
                        + "password!");
                password.setText("");
                verifyPassword.setText("");
                password.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill all the fields!");
        }
    }//GEN-LAST:event_okActionPerformed

