    private boolean[] checkFields() {
        boolean[] flag = new boolean[2];
        flag[0] = true;
        flag[1] = true;

        if (name.getText().equals("")) {
            flag[0] = false;
        }

        if (email.getText().equals("")) {
            flag[0] = false;
        } else {
            try {
                String[] emailParts = email.getText().split("@");
                if (emailParts.length != 2) {
                    flag[0] = false;
                    flag[1] = false;
                    JOptionPane.showMessageDialog(this, "Invalid email.");
                }
            } catch (Exception ex) {
                flag[0] = false;
                flag[1] = false;
                JOptionPane.showMessageDialog(this, "Invalid email.");
            }
        }

        if (mobileNo.getText().equals("")) {
            flag[0] = false;
        } else {
            if (mobileNo.getText().length() != 10) {
                flag[0] = false;
                flag[1] = false;
                JOptionPane.showMessageDialog(this, "Length of Mobile shold be 10 digits");
            } else {
                try {
                    int number = Integer.parseInt(mobileNo.getText());
                } catch (Exception ex) {
                    flag[0] = false;
                    flag[1] = false;
                    JOptionPane.showMessageDialog(this, "Enter a valid phone number");
                }
            }
        }

        if (nicNo.getText().equals("")) {
            flag[0] = false;
        }

        if (password.getPassword().equals("")) {
            flag[0] = false;
        }

        if (verifyPassword.getPassword().equals("")) {
            flag[0] = false;
        }
        return flag;
    }

    private boolean registerCustomer(String name, int mobile, String email,
            String nic, String pass, int gender) {
        boolean flag = false;
        Connection con;
        int rs;
        String password = SHA.getSHAPassword(pass);
        try {
            con = Database.getConnected();
            String query = "INSERT INTO customer_login(customer_name, phone, "
                    + "email, nic_number, password, gender) "
                    + "VALUES('" + name + "','" + mobile + "','" + email
                    + "','" + nic + "','" + password + "','" + gender + "')";
            rs = Database.updateQuery(con, query);
            if (rs == 1) {
                flag = true;
            }
        } catch (SQLException | ClassNotFoundException ex) {
        }
        return flag;
    }


