    private void OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKActionPerformed
        boolean[] checkInputs;
        checkInputs = checkFields();
        if (checkInputs[0]) {
            try {
                String name = this.name.getText();
                String email = this.email.getText();
                int mobile = Integer.parseInt(mobileNo.getText());
                String nic = nicNo.getText();
                String password1 = String.valueOf(password.getPassword());
                String password2 = String.valueOf(verifyPassword.getPassword());
                int gender = (this.gender.getSelectedIndex() == 0) ? 1 : 0;
                if (password1.equals(password2)) {
                    boolean flag = registerCustomer(name, mobile, email, nic, password1, gender);
                    if (!flag) {
                        JOptionPane.showMessageDialog(this, "Error! Try again "
                                + "with correct data. If you had an old account,"
                                + " use forgotten password option in startup screen");
                    } else {
                        JOptionPane.showMessageDialog(this, "Customer added successfully!");

                        StartBooking x = new StartBooking(email);
                        x.setVisible(true);
                        this.dispose();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Please enter correct password!");
                    password.setText("");
                    verifyPassword.setText("");
                    password.requestFocus();
                }
            } catch (Exception e) {
            }

        } else if (checkInputs[1]) {
            JOptionPane.showMessageDialog(this, "Please fill all the fields with valid data!");
        }
    }//GEN-LAST:event_OKActionPerformed

