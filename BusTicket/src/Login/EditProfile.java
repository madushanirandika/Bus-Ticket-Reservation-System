    private boolean[] checkFields() {
        boolean[] flag = new boolean[2];
        flag[0] = true;
        flag[1] = true;

        if (name.getText().equals("")) {
            flag[0] = false;
        }

        if (phoneNo.getText().equals("")) {
            flag[0] = false;
        } else if (phoneNo.getText().length() == 10) {
            if (!InputChecker.isANumber(phoneNo.getText())) {
                flag[0] = false;
                flag[1] = false;
                JOptionPane.showMessageDialog(this, "Invalid Phone Number");
            }
        } else {
            flag[0] = false;
            flag[1] = false;
            JOptionPane.showMessageDialog(this, "Length of Pnone Number must be 10 digits");
        }

        if (nicNo.getText().equals("")) {
            flag[0] = false;
        }

        return flag;
    }

    private void addData(String email) {
        try {
            Connection con;
            con = Database.getConnected();
            String query = "SELECT * FROM `customer_login` WHERE `email`='"
                    + email + "'";
            ResultSet rs = Database.executeQuery(con, query);
            rs.next();

            name.setText(rs.getString(1));
            phoneNo.setText("0" + rs.getString(3));
            nicNo.setText(rs.getString(4));

            if (rs.getString(6).equals("0")) {
                gender.setSelectedItem("Female");
            } else {
                gender.setSelectedItem("Male");
            }
        } catch (SQLException | ClassNotFoundException ex) {
        }
    }

    private boolean editCustomerProfile(String email, String name1, int mobile1, String nic1, int gender1) {
        boolean flag = false;
        Connection con;
        int rs;
        try {
            con = Database.getConnected();

            String query = "UPDATE `customer_login`  SET `customer_name`='" + name1 + "' "
                    + ",  `phone`=" + mobile1 + ", `nic_number` = '" + nic1 + "', `gender`=" + gender1 + " "
                    + "WHERE `email`='" + email + "'";
            rs = Database.updateQuery(con, query);
            if (rs == 1) {
                flag = true;
            }
        } catch (SQLException | ClassNotFoundException ex) {

        }
        return flag;
    }

