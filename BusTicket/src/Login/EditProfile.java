    private void editDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editDataActionPerformed
        boolean[] checkInputs;
        checkInputs = checkFields();
        if (checkInputs[0] && checkInputs[1]) {
            try {
                String name1 = name.getText();
                int mobile1 = Integer.parseInt(phoneNo.getText());
                String nic1 = nicNo.getText();
                int gender1 = gender.getSelectedIndex();

                boolean flag = editCustomerProfile(customerEmail, name1, mobile1, nic1, gender1);
                if (!flag) {
                    JOptionPane.showMessageDialog(this, "Error! Try again ");
                } else {
                    JOptionPane.showMessageDialog(this, "Your profile updated successfully!");
                }
            } catch (Exception e) {
            }
        } else if (checkInputs[1]) {
            JOptionPane.showMessageDialog(this, "Please fill all the fields with valid data!");
        }
        StartBooking x = new StartBooking(customerEmail);
        x.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_editDataActionPerformed

