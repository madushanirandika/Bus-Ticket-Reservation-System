    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        if (!(newPass.getText().equals("") || oldPass.getText().equals("") || verifyPass.getText().equals(""))) {
            String password1 = String.valueOf(newPass.getPassword());
            String password2 = String.valueOf(verifyPass.getPassword());
            String password3 = String.valueOf(oldPass.getPassword());
            if (password1.equals(password2)) {

                boolean flag = changeUserPassword(password1, password3);

                if (!flag) {
                    JOptionPane.showMessageDialog(this, "Error! Try again with correct data");
                    oldPass.setText("");
                    newPass.setText("");
                    verifyPass.setText("");
                    oldPass.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(this, "Change password successfully!");
                    this.dispose();
                    EditProfile x = new EditProfile(customerEmail);
                    x.setVisible(true);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Please enter correct password!");
                newPass.setText("");
                verifyPass.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill all the fields!");
        }
    }//GEN-LAST:event_okActionPerformed

