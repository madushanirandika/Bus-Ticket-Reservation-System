    private boolean changeUserPassword(String password1, String password3) {
        boolean flag = false;
        Connection con;
        int rs;
        String passWord1 = SHA.getSHAPassword(password1);
        String passWord3 = SHA.getSHAPassword(password3);
        try {
            con = Database.getConnected();
            String query = "UPDATE customer_login SET password='" + passWord1 + "' "
                    + "WHERE email='" + customerEmail 
                    + "' AND password='" + passWord3 + "'";
            rs = Database.updateQuery(con, query);
            if (rs == 1) {
                flag = true;
            }
        } catch (SQLException | ClassNotFoundException ex) {

        }
        return flag;
    }
